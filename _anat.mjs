function mirrorPath(d, cx = CX) {
  const toks = d.match(/[a-zA-Z]|-?\d*\.?\d+/g) || [];
  let out = [], ci = 0;
  for (const tk of toks) {
    if (/[a-zA-Z]/.test(tk)) { out.push(tk); ci = 0; }
    else { const n = parseFloat(tk); out.push(ci % 2 === 0 ? +(2 * cx - n).toFixed(1) : n); ci++; }
  }
  return out.join(" ");
}
